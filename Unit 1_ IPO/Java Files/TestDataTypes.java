public class TestDataTypes
{
  public static void main(String[] arg)
  {
    System.out.println("Part 1"); //Divider
    
    System.out.println("23");
    System.out.println(23);
    System.out.println("2" + "3");
    System.out.println(2 + 3);
    
    System.out.println("\nPart 2"); //Divider
    
    System.out.println(2 + 3);
    System.out.println(2 - 3);
    System.out.println(2 * 3);
    System.out.println(2 / 3);
    
    System.out.println("\nPart 3"); //Divider
    
    System.out.println(2.0 + 3.0);
    System.out.println(2.0 - 3.0);
    System.out.println(2.0 * 3.0);
    System.out.println(2.0 / 3.0);
    
    System.out.println("\nPart 4"); //Divider
    System.out.println(5.0 * 4.0 + 3.0 / 2.0);
    System.out.println(5.0 * (4.0 + 3.0) / 2.0);
    System.out.println(5.0 * (4.0 + 3.0 / 2.0));
    System.out.println((5.0 * 3.0 + 3.0) / 2.0);
  } // end main
} // end class